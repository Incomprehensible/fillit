#include "fillit.h"

int	find_x_2(char **fig, int num)
{
	static int x;
	static int y;

	x = (num == 0 ? 0 : x);
	y = (num == 0 ? 0 : y);
	while (x < 4)
	{
		y = 0;
		while(y < 4)
		{
			if (fig[x][y] == '#')
			{
				y++;
				return (y);
			}
			y++;
		}
		x++;
	}
	return (y);
}
